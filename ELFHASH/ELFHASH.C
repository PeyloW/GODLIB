/*-----------------------------------------------------------------------------------*
* FUNCTION : ElfHash_BuildHash( const char * apString )
* ACTION   : ElfHash_BuildHash
* CREATION : 11.02.2005 PNK
*-----------------------------------------------------------------------------------*/

U32	ElfHash_BuildHash( const char * apString )
{
	U32	lHash;
	U32	lIndex;
	U32	lTemp;

	lHash  = 0L;
	if( apString )
	{
		lIndex = 0;
		while( apString[ lIndex ] ) 
		{
			char lChr = apString[ lIndex ];
			if( (lChr>='A') && (lChr<='Z') )
			{
				lChr -= ('A'-'a');
			}
			lHash = ( lHash << 4L ) + lChr;
			lTemp = lHash & 0xF0000000L;
			if ( lTemp  )
			{
				lHash ^= lTemp >> 24L;
			}
			lHash &= ~lTemp;
			lIndex++;
		}
	}

    return( lHash );		
}


